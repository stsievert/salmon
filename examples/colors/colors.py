def _fmt_color(c):
    c = hex(int(255 * c))[2:]
    if len(c) == 1:
        c = f"0{c}"
    assert len(c) == 2
    return c
def _convert(r, g, b):
    r, g, b = map(_fmt_color, [r, g, b])
    return f"#{r}{g}{b}"

